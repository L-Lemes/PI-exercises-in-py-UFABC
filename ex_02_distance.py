xA = float(input("Digite o valor do ponto xA: "))
yA = float(input("Digite o valor do ponto yA: "))
xB = float(input("Digite o valor do ponto xB: "))
yB = float(input("Digite o valor do ponto yB: "))
         
distancia = ((xA - xB)**2 + (yA - yB)**2)**(1/2)

print("Distancia = ", round(distancia, 1))

